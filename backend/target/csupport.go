package target

import "bytes"

// RenderC89Support emits the machine preamble plus arithmetic helpers whose
// behavior is defined entirely in terms of unsigned C operations. Generated C
// represents a Renvo signed int by its modular bit pattern in renvo_uint, so it
// never depends on the C implementation's signed representation or overflow.
func (p CMachineProfile) RenderC89Support() ([]byte, error) {
	preamble, err := p.RenderC89Preamble()
	if err != nil {
		return nil, err
	}
	var out bytes.Buffer
	out.Write(preamble)
	out.WriteString("\n#ifndef RENVO_C89_ARITHMETIC\n")
	out.WriteString("#define RENVO_C89_ARITHMETIC 1\n")
	out.WriteString("#ifndef RENVO_C_DIVZERO\n")
	out.WriteString("#define RENVO_C_DIVZERO() ((void)0)\n")
	out.WriteString("#endif\n")
	out.WriteString("#define renvo_sex8 rgx08\n")
	out.WriteString("#define renvo_sex16 rgx16\n")
	out.WriteString("#define renvo_slt rgslt\n")
	out.WriteString("#define renvo_sle rgsle\n")
	out.WriteString("#define renvo_sdiv rgsdv\n")
	out.WriteString("#define renvo_srem rgsrm\n")
	out.WriteString("#define renvo_shl rgshl\n")
	out.WriteString("#define renvo_ushr rguhr\n")
	out.WriteString("#define renvo_sshr rgshr\n")
	out.WriteString("renvo_uint rgx08(renvo_u8 value)\n")
	out.WriteString("{\n")
	out.WriteString("    renvo_uint result;\n")
	out.WriteString("    result = (renvo_uint)value;\n")
	out.WriteString("    if ((value & 128U) != 0U) result |= ~((renvo_uint)255U);\n")
	out.WriteString("    return result;\n")
	out.WriteString("}\n")
	out.WriteString("renvo_uint rgx16(renvo_u16 value)\n")
	out.WriteString("{\n")
	out.WriteString("    renvo_uint result;\n")
	out.WriteString("    result = (renvo_uint)value;\n")
	out.WriteString("    if ((value & 32768UL) != 0UL) result |= ~((renvo_uint)65535UL);\n")
	out.WriteString("    return result;\n")
	out.WriteString("}\n")
	out.WriteString("int rgslt(renvo_uint left, renvo_uint right)\n")
	out.WriteString("{\n")
	out.WriteString("    renvo_uint sign;\n")
	out.WriteString("    sign = (renvo_uint)1U << (RENVO_C_LANGUAGE_INT_BITS - 1);\n")
	out.WriteString("    if (((left ^ right) & sign) != 0U) return (left & sign) != 0U;\n")
	out.WriteString("    return left < right;\n")
	out.WriteString("}\n")
	out.WriteString("int rgsle(renvo_uint left, renvo_uint right)\n")
	out.WriteString("{\n")
	out.WriteString("    return left == right || renvo_slt(left, right);\n")
	out.WriteString("}\n")
	out.WriteString("renvo_uint rgsdv(renvo_uint left, renvo_uint right)\n")
	out.WriteString("{\n")
	out.WriteString("    renvo_uint sign;\n")
	out.WriteString("    renvo_uint left_magnitude;\n")
	out.WriteString("    renvo_uint right_magnitude;\n")
	out.WriteString("    renvo_uint quotient;\n")
	out.WriteString("    sign = (renvo_uint)1U << (RENVO_C_LANGUAGE_INT_BITS - 1);\n")
	out.WriteString("    if (right == 0U) { RENVO_C_DIVZERO(); return 0U; }\n")
	out.WriteString("    left_magnitude = (left & sign) != 0U ? (~left + 1U) : left;\n")
	out.WriteString("    right_magnitude = (right & sign) != 0U ? (~right + 1U) : right;\n")
	out.WriteString("    quotient = left_magnitude / right_magnitude;\n")
	out.WriteString("    if (((left ^ right) & sign) != 0U) quotient = ~quotient + 1U;\n")
	out.WriteString("    return quotient;\n")
	out.WriteString("}\n")
	out.WriteString("renvo_uint rgsrm(renvo_uint left, renvo_uint right)\n")
	out.WriteString("{\n")
	out.WriteString("    renvo_uint sign;\n")
	out.WriteString("    renvo_uint left_magnitude;\n")
	out.WriteString("    renvo_uint right_magnitude;\n")
	out.WriteString("    renvo_uint remainder;\n")
	out.WriteString("    sign = (renvo_uint)1U << (RENVO_C_LANGUAGE_INT_BITS - 1);\n")
	out.WriteString("    if (right == 0U) { RENVO_C_DIVZERO(); return 0U; }\n")
	out.WriteString("    left_magnitude = (left & sign) != 0U ? (~left + 1U) : left;\n")
	out.WriteString("    right_magnitude = (right & sign) != 0U ? (~right + 1U) : right;\n")
	out.WriteString("    remainder = left_magnitude % right_magnitude;\n")
	out.WriteString("    if ((left & sign) != 0U) remainder = ~remainder + 1U;\n")
	out.WriteString("    return remainder;\n")
	out.WriteString("}\n")
	out.WriteString("renvo_uint rgshl(renvo_uint value, renvo_uint count)\n")
	out.WriteString("{\n")
	out.WriteString("    if (count >= (renvo_uint)RENVO_C_LANGUAGE_INT_BITS) return 0U;\n")
	out.WriteString("    return value << count;\n")
	out.WriteString("}\n")
	out.WriteString("renvo_uint rguhr(renvo_uint value, renvo_uint count)\n")
	out.WriteString("{\n")
	out.WriteString("    if (count >= (renvo_uint)RENVO_C_LANGUAGE_INT_BITS) return 0U;\n")
	out.WriteString("    return value >> count;\n")
	out.WriteString("}\n")
	out.WriteString("renvo_uint rgshr(renvo_uint value, renvo_uint count)\n")
	out.WriteString("{\n")
	out.WriteString("    renvo_uint sign;\n")
	out.WriteString("    renvo_uint result;\n")
	out.WriteString("    sign = (renvo_uint)1U << (RENVO_C_LANGUAGE_INT_BITS - 1);\n")
	out.WriteString("    if (count == 0U) return value;\n")
	out.WriteString("    if (count >= (renvo_uint)RENVO_C_LANGUAGE_INT_BITS) return (value & sign) != 0U ? ~(renvo_uint)0U : 0U;\n")
	out.WriteString("    result = value >> count;\n")
	out.WriteString("    if ((value & sign) != 0U) result |= ~(renvo_uint)0U << (RENVO_C_LANGUAGE_INT_BITS - count);\n")
	out.WriteString("    return result;\n")
	out.WriteString("}\n")
	out.WriteString("#endif\n")
	return out.Bytes(), nil
}
